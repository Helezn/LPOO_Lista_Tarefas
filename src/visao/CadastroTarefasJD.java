    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       
        if(!txtTarefa.getText().trim().isEmpty())
            tarefa.setTarefa(txtTarefa.getText().trim());
        if(!txtDescricao.getText().trim().isEmpty())
            tarefa.setDescricao(txtDescricao.getText().trim());
        if(cmbCategoria.getSelectedItem()!=null)
            tarefa.setCategoria((CategoriaLista)cmbCategoria.getSelectedItem());
        if(cmbPrioridade.getSelectedItem()!=null)
            tarefa.setPrioridade((Prioridade)cmbPrioridade.getSelectedItem());
        
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed


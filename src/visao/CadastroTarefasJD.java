   public void carregarCategorias(){
       cmbCategoria.removeAll();
       for(CategoriaLista cat: CategoriaLista.values()){
           cmbCategoria.addItem(cat);
       }
   }
   
   public void carregarPrioridades(){
       cmbPrioridade.removeAll();
       for(Prioridade pri: Prioridade.values()){
           cmbPrioridade.addItem(pri);
       }
   }

